<p>(a) A vessel at anchor shall exhibit where it can best be seen:</p>
<ul>
	<li>(i) in the fore part, an all-round white light or one ball;</li>
	<li>(ii) at or near the stern and at a lower level than the light prescribed in sub- paragraph (i), an all-round white light</li>
</ul>
<p>(b) A vessel of less than 50 metres in length may exhibit an all-round white light where it can best be seen instead of the lights prescribed in paragraph (a) of this Rule.</p>
<p>(c) A vessel at anchor may, and a vessel of 100 metres and more in length shall, also use the available working or equivalent lights to illuminate her decks.</p>
<p>(d) A vessel aground shall exhibit the lights prescribed in paragraph (a) or (b) of this Rule and in addition, where they can best be seen:</p>
<ul>
	<li>(i) two all-round red lights in a vertical line;</li>
	<li>(ii) three balls in a vertical line.</li>
</ul>
<p>(e) A vessel of less than 7 metres in length, when at anchor, not in or near a narrow channel, fairway or anchorage, or where other vessels normally navigate, shall not be required to exhibit the lights or shape prescribed in paragraphs (a), (b) of this Rule.</p>
<p>(f) A vessel of less than 12 metres in length, when aground, shall not be required to exhibit the lights or shapes prescribed in sub-paragraphs (d)(i) and (ii) of this Rule.</p>