<p>(a) A vessel engaged on pilotage duty shall exhibit:</p>
<ul>
	<li>(i) at or near the masthead, two all-round lights in a vertical line, the upper being white and the lower red;</li>
	<li>(ii) when underway, in addition, sidelight and a sternlight;</li>
	<li>(iii) when at anchor, in addition to the lights prescribed in subparagraph (i), the light, lights or shape prescribed in Rule 30 for vessels at anchor.</li>
</ul>
<p>(b) A pilot vessel when not engaged on pilotage duty shall exhibit the lights or shapes prescribed for a similar vessel of her length.</p>