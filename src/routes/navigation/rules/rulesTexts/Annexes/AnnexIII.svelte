<p class="text-weight-l text-size-m">1. Whistles</p>
<p>(a) Frequencies and range of audibility.</p>
<p>
	The fundamental frequency of the signal shall lie within the range 70-700Hz.
	The range of audibility of the signal from a whistle shall be determined by
	those frequencies, which may include the fundamental and/or one or more higher
	frequencies, which lie within the range 180-700Hz (+/-1%) for a vessel of 20
	metres or more in length, or 180-2100Hz (+/-1%) for a vessel of less than 20
	metres in length and which provide the sound pressure levels specified in
	paragraph 1(c) below.
</p>
<p>(b) Limits of fundamental frequencies.</p>
<p>
	To ensure a wide variety of whistle characteristics, the fundamental frequency
	of a whistle shall be between the following limits :
</p>
<ul>
	<li>(i) 70 - 200 Hz, for a vessel 200 metres or more in length;</li>
	<li>
		(ii) 130 - 350 Hz, for a vessel 75 metres but less than 200 metres in
		length;
	</li>
	<li>(iii) 250 - 700 Hz, for a vessel less than 75 metres in length.</li>
</ul>
<p>(c) Sound signal intensity and range of audibility.</p>
<p>
	A whistle fitted in a vessel shall provide, in the direction of maximum.
	intensity of the whistle and at a distance of 1 metre from it, a sound
	pressure level in at least one 1/3rd-octave band within the range of
	frequencies 180-700Hz (+/-1%) for a vessel of 20 metres or more in length, or
	180-2100Hz (+/-1%) for a vessel of less than 20 metres in length, of not less
	than the appropriate figure given in the table below.
</p>
