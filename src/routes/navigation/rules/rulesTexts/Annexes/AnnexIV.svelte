<p class="text-weight-l text-size-m">Distress Signals</p>
<p>
	1. The following signals, used or exhibited either together or separately,
	indicate distress and need of assistance:
</p>
<ul>
	<li>
		(a) a gun or other explosive signal fired at intervals of about a minute;
	</li>
	<li>(b) a continuous sounding with any fog-signalling apparatus;</li>
	<li>
		(c) rockets or shells, throwing red stars fired one at a time at short
		intervals;
	</li>
	<li>
		(d) a signal made by radiotelegraphy or by any other signalling method
		consisting of the group &hellip;---&hellip; (SOS) in the Morse Code;
	</li>
	<li>
		(e) a signal sent by radiotelephony consisting of the spoken word "Mayday";
	</li>
	<li>(f) the International Code Signal of distress indicated by N. C.;</li>
	<li>
		(g) a signal consisting of a square flag having above or below it a ball or
		anything resembling a ball;
	</li>
	<li>
		(h) flames on the vessel (as from a burning tar barrel, oil barrel, etc.);
	</li>
	<li>(i) a rocket parachute flare or a hand flare showing a red light;</li>
	<li>(j) a smoke signal giving off orange-coloured smoke;</li>
	<li>
		(k) slowly and repeatedly raising and lowering arms outstretched to each
		side;
	</li>
	<li>(l) the radiotelegraph alarm signal;</li>
	<li>(m) the radiotelephone alarm signal;</li>
	<li>
		(n) signals transmitted by emergency position-indicating radio beacons.
	</li>
	<li>
		(o) approved signals transmitted by radiocommunication systems, including
		survival craft radar transponders.
	</li>
</ul>
<p>
	2. The use or exhibition of any of the foregoing signals except for the
	purpose of indicating distress and need of assistance and the use of other
	signals which may be confused with any of the above signals is prohibited.
</p>
<p>
	3. Attention is drawn to the relevant sections of the International Code of
	Signals, the Merchant Ship Search and Rescue Manual and the following signals:
</p>
<ul>
	<li>
		(a) a piece of orange-coloured canvas with either a black square and circle
		or other appropriate symbol (for identification from the air);
	</li>
	<li>(b) a dye marker.</li>
</ul>
