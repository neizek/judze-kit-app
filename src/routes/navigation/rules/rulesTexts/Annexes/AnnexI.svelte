<p class="text-weight-l text-size-m">8. Intensity of lights</p>
<p>
	(a) The minimum luminous intensity of lights shall be calculated by using the
	formula:
</p>
<ul>
	<li>I = 3.43 X 10<sup>6</sup> X T X D<sup>2</sup> X K<sup>-D</sup></li>
</ul>
<p>where I is luminous intensity in candelas under service conditions,</p>
<ul>
	<li>T is threshold factor 2×10<sup>-7</sup> lux,</li>
	<li>
		D is range of visibility (luminous range) of the light in nautical miles,
	</li>
	<li>K is atmospheric transmissivity.</li>
</ul>
<p>
	For prescribed lights the value of K shall be 0.8, corresponding to a
	meteorological visibility of approximately 13 nautical miles.
</p>
