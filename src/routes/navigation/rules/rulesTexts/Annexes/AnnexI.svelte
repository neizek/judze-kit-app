<p class="text-weight-l text-size-m">POSITIONING AND TECHNICAL DETAILS OF LIGHTS AND SHAPES</p>
<p class="text-weight-l text-size-m">1. Definition</p>
<p>
	The term 'height above the hull' means height above the uppermost continuous deck. This height
	shall be measured from the position vertically beneath the location of the light.
</p>
<p class="text-weight-l text-size-m">2. Vertical positioning and spacing of lights</p>
<p>
	(a) On a power-driven vessel of 20 metres or more in length the masthead lights shall be placed
	as follows:
</p>
<ul>
	<li>
		(i) the forward masthead light, or if only one masthead light is carried, then that light,
		at a height above the hull of not less than 6 meters, and, if the breadth of the vessel
		exceeds 6 meters, then at a height above the hull not less than such breadth, so however
		that the light need not be placed at a greater height above the hull than 12 metres;
	</li>
	<li>
		(ii) when two masthead lights are carried the after one shall be at least 4.5 metres
		vertically higher than the forward one.
	</li>
</ul>
<p>
	(b) The vertical separation of mastheadlights of power-driven vessels shall be such that in all
	normal conditions of trim the after light will be seen over and separate from the forward light
	at a distance of 1,000 meters from the stem when viewed from sea level.
</p>
<p>
	(c) The masthead light of a power-driven vessel of 12 metres but less than 20 metres in length
	shall be placed at a height above the gunwale of not less than 2.5 metres.
</p>
<p>
	(d) A power-driven vessel of less than 12 metres in length may carry the uppermost light at a
	height of less than 2.5 metres above the gunwale. When however a masthead light is carried in
	addition to sidelights and a sternlight or the all-round light prescribed in rule 23(c) (i) is
	carried in addition to sidelights, then such masthead light or all-round light shall be carried
	at least 1 metre higher than the sidelights.
</p>
<p>
	(e) One of the two or three masthead lights prescribed for a power-driven vessel when engaged in
	towing or pushing another vessel shall be placed in the same position as either the forward
	masthead light or the after masthead light: provided that, if carried on the aftermast, the
	lowest after masthead light shall be at least 4.5 metres vertically higher than the forward
	masthead light.
</p>
<p>(f)</p>
<ul>
	<li>
		(i) The masthead lights prescribed in Rule 23 (a) shall be so placed as to be above and
		clear of all other lights and obstructions except as described in sub- paragraph (ii)
	</li>
	<li>
		(ii) When it is impracticable to carry the all-round lights prescribed by Rule 27(b) (i) or
		Rule 28 below the masthead lights, they may be carried above the after masthead light(s) or
		vertically in between the forward masthead light(s) and after masthead light(s), provided
		that in the latter case the requirement of Section 3 (c) of this Annex shall be complied
		with.
	</li>
</ul>
<p>
	(g) The sidelights of a power-driven vessel shall be placed at a height above the hull not
	greater than three quarters of that of the forward masthead light. They shall not be so low as
	to be interfered with by deck lights.
</p>
<p>
	(h) The sidelights, if in a combined lantern and carried on a power-driven vessel of less than
	20 metres in length, shall be placed not less than 1 metre below the masthead light.
</p>
<p>
	(i) When the Rules prescribe two or three lights to be carried in a vertical line, they shall be
	spaced as follows:
</p>
<ul>
	<li>
		(i) on a vessel of 20 metres in length or more such lights shall be spaced not less than 2
		metres apart, and the lowest of these lights shall, except where a towing light is required,
		be placed at a height of not less than 4 m above the hull;
	</li>
	<li>
		(ii) on a vessel of less than 20 metres in length such lights shall be spaced not less than
		1 metre apart and the lowest of these lights shall, except where a towing light is required,
		be placed at a height of not less than 2 metres above the gunwale.
	</li>
	<li>(iii) when three lights are carried they shall be equally spaced.</li>
</ul>
<p>
	(j) The lower of the two all-round lights prescribed for a vessel when engaged in fishing shall
	be at a height above the sidelights not less than twice the distance between the two vertical
	lights.
</p>
<p>
	(k) The forward anchor light prescribed in Rule 30(a)(i), when two are carried, shall not be
	less than 4.5 metres above the after one. On a vessel of 50 metres or more in length this
	forward anchor light shall be placed at a height of not less than 6 metres above the hull.
</p>
<p class="text-weight-l text-size-m">3. Horizontal positioning and spacing of lights</p>
<p>
	(a) When two masthead lights are prescribed for a power-driven vessel, the horizontal distance
	between them shall not be less than one half of the length of the vessel but need not be more
	than 100 m. The forward light shall be placed not more than one quarter of the length of the
	vessel from the stem.
</p>
<p>
	(b) On a power-driven vessel of 20 m or more in length the sidelights shall not be placed in
	front of the forward masthead lights. They shall be placed at or near the side of the vessel.
</p>
<p>(Refer to IACS UI COLREG3)</p>
<p>
	(c) When the lights prescribed in Rule 27(b)(i) or Rule 28 are placed vertically between the
	forward masthead light(s) and the after masthead light(s) these all-round lights shall be placed
	at a horizontal distance of not less than 2 m from the fore and after centreline of the vessel
	in the athwartship direction.
</p>
<p>
	(d) When only one masthead light is prescribed for a power driven vessel, this light shall be
	exhibited forward of amidships; except that a vessel of less than 20 m in length need not
	exhibit this light forward of amidships but shall exhibit it as far forward as is practicable.
</p>
<p class="text-weight-l text-size-xxs">
	MSC.1/Circ.1260 - UNIFIED INTERPRETATIONS OF COLREG 1972, AS AMENDED
</p>
<p class="text-weight-l text-size-m">
	Annex I, section 3(b) - Horizontal positioning and spacing of lights
</p>
<p>
	The term “near the side” is interpreted as being a distance of not more than 10% of the breadth
	of the vessel inboard from the side, up to a maximum of 1 metre. Where the application of above
	requirement is impractical (e.g., small ships with superstructure of reduced width) exemption
	may be given on the basis of the Flag Authority acceptance.
</p>
<p class="text-weight-l text-size-m">
	4. Details of location of direction-indicating lights for fishing vessels, dredgers and vessels
	engaged in underwater operations
</p>
<p>
	(a) The light indicating the direction of the outlying gear from a vessel engaged in fishing as
	prescribed in Rule 26(c)(ii) shall be placed at a horizontal distance of not less than 2 metres
	and not more than 6 metres away from the two all-round red and white lights. This light shall be
	placed not higher than the all-round white light prescribed in Rule 26(c)(i) and not lower than
	the sidelights.
</p>
<p>
	(b) The lights and shapes on a vessel engaged in dredging or underwater operations to indicate
	the obstructed side and/or the side on which it is safe to pass, as prescribed in Rule 27(d)(i)
	and (ii), shall be placed at the maximum practical horizontal distance, but in no case less than
	2 metres, from the lights or shapes prescribed in Rule 27(b)(i) and (ii). In no case shall the
	upper of these lights or shapes be at a greater height than the lower of the three lights or
	shapes prescribed in Rule 27(b)(i) and (ii).
</p>
<p class="text-weight-l text-size-m">5. Screens for sidelights</p>
<p>
	The sidelights of vessels of 20 metres or more in length shall be fitted with inboard screens
	painted matt black, and meeting the requirements of Section 9 of this Annex. On vessels of less
	than 20metres in length the sidelights, if necessary to meet the requirements of Section 9 of
	this Annex, shall be fitted with inboard matt black screens. With a combined lantern, using a
	single vertical filament and a very narrow division between the green and red sections, external
	screens need not be fitted.
</p>
<p class="text-weight-l text-size-m">6. Shapes</p>
<p>(a) Shapes shall be black and of the following sizes:</p>
<ul>
	<li>(i) a ball shall have a diameter of not less than 0.6 metre;</li>
	<li>
		(ii) a cone shall have a base diameter of not less than 0.6 metre and a height equal to its
		diameter;
	</li>
	<li>
		(iii) a cylinder shall have a diameter of at least 0.6 metre and a height of twice its
		diameter;
	</li>
	<li>
		(iv) a diamond shape shall consist of two cones as defined in (ii) above having a common
		base.
	</li>
</ul>
<p>(b) The vertical distance between shapes shall be at least 1.5 metres.</p>
<p>
	(c) In a vessel of less than 20 metres in length shapes of lesser dimensions but commensurate
	with the size of the vessel may be used and the distance apart may be correspondingly reduced.
</p>
