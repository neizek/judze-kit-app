<p class="text-weight-l text-size-m">10. Vertical sectors</p>
<p>
	(a) The vertical sectors of electric lights as fitted, with the exception of lights on sailing
	vessels underway shall ensure that:
</p>
<ul>
	<li>
		(i) at least the required minimum intensity is maintained at all angles from 5 degrees above
		to 5 degrees below the horizontal;
	</li>
	<li>
		(ii) at least 60 per cent of the required minimum intensity is maintained from 7.5 degrees
		above to 7.5 degrees below the horizontal.
	</li>
</ul>
<p>
	(b) In the case of sailing vessels underway the vertical sectors of electric lights as fitted
	shall ensure that:
</p>
<ul>
	<li>
		(i) at least the required minimum intensity is maintained at all angles from 5 degrees above
		to 5 degrees below the horizontal;
	</li>
	<li>
		(ii) at least 50 per cent of the required minimum intensity is maintained from 25 degrees
		above to 25 degrees below the horizontal.
	</li>
</ul>
<p>
	(c) In the case of lights other than electric these specifications shall be met as closely as
	possible.
</p>

<p class="text-weight-l text-size-m">11. Intensity of non-electric lights</p>
<p>
	Non-electric lights shall so far as practicable comply with the minimum intensities, as
	specified in the Table given in Section 8 of this Annex.
</p>
<p class="text-weight-l text-size-m">12. Manoeuvring light</p>
<p>
	Notwithstanding the provisions of paragraph 2 (f) of this Annex the manoeuvring light described
	in Rule 34(b) shall be placed in the same fore and aft vertical plane as the masthead light or
	lights and, where practicable, at a minimum height of 2 metres vertically above the forward
	masthead light, provided that it shall be carried not less than 2 meters vertically above or
	below the after masthead light. On a vessel where only one masthead light is carried the
	manoeuvring light, if fitted, shall be carried where it can best be seen, not less than 2 metres
	vertically apart from the masthead light.
</p>
<p class="text-weight-l text-size-m">13. High Speed Craft*</p>
<p class="text-weight-l text-size-xxs">
	(This section shall enter into force on 29 November 2003 by Resolution A.910(22))
</p>
<p>
	* Refer to the International Code of Safety for High-Speed Craft, 1994 and the International
	Code of Safety for High-Speed Craft, 2000.
</p>
<p>
	(a) The masthead light of high-speed craft may be placed at a height related to the breadth of
	the craft lower than that prescribed in paragraph 2(a)(i) of this annex, provided that the base
	angle of the isosceles triangles formed by the sidelights and masthead light, when seen in end
	elevation, is not less than 27 degrees.
</p>
<p>
	(b) On high-speed craft of 50 metres or more in length, the vertical separation between foremast
	and mainmast light of 4.5 metres required by paragraph 2(a)(ii) of this annex may be modified
	provided that such distance shall not be less than the value determined by the following
	formula:
</p>
<p class="text-centered text-size-m">y = (a + 17ψ)C / 1000 + 2</p>
<p>where:</p>
<ul>
	<li>y is the height of the mainmast light above the fore mast light in metres;</li>
	<li>
		a is the height of the foremast light above the water surface in service condition in
		metres;
	</li>
	<li>Ψ is the trim in service condition in degrees;</li>
	<li>C is the horizontal separation of masthead lights in metres.</li>
</ul>
<p class="text-weight-l text-size-m">14. Approval</p>
<p>
	The construction of lanterns and shapes and the installation of lanterns on board the vessel
	shall be to the satisfaction of the appropriate authority of the State whose flag the vessel is
	entitled to fly.
</p>

