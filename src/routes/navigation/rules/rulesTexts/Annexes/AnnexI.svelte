<p>
	NOTE: The maximum luminous intensity of navigation lights should be limited to avoid undue
	glare.
</p>
<p>This shall not be achieved by a variable control of the luminous intensity.</p>
<p>(a)</p>
<p>
	(i) In the forward direction, sidelights as fitted on the vessel shall show the minimum required
	intensities. The intensities must decrease to reach practical cut-off between 1 degree and 3
	degrees outside the prescribed sectors.
</p>
<p>
	(ii) For sternlights and masthead lights and at 22.5 degrees abaft the beam for sidelights, the
	minimum required intensities shall be maintained over the arc of the horizon up to 5 degrees
	within the limits of the sectors prescribed in Rule 21. From 5 degrees within the prescribed
	sectors the intensity may decrease by 50 percent up to the prescribed limits; it shall decrease
	steadily to reach practical cut-off at not more than 5 degrees outside the prescribed sectors.
</p>
<p>(b)</p>
<p>
	(i) All-round lights shall be so located as not to be obscured by masts, topmasts or structures
	within angular sectors of more than 6 degrees, except anchor lights prescribed in Rule 30, which
	need not be placed at an impracticable height above the hull.
</p>
<p>
	(ii) If it is impracticable to comply with paragraph (b)(i) of this section by exhibiting only
	one all-round light, two all-round lights shall be used suitably positioned or screened so that
	they appear, as far as practicable, as one light at a distance of one mile.
</p>
