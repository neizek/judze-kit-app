		X T X D
		<sup>2</sup>
		X K
		<sup>-D</sup>
	</li>
</ul>
<p>where I is luminous intensity in candelas under service conditions,</p>
<ul>
	<li>
		T is threshold factor 2×10
		<sup>-7</sup>
		lux,
	</li>
	<li>D is range of visibility (luminous range) of the light in nautical miles,</li>
	<li>K is atmospheric transmissivity.</li>
</ul>
