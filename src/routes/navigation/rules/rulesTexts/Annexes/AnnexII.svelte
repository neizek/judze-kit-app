<p class="text-weight-l text-size-m">
	ADDITIONAL SIGNALS FOR FISHING VESSELS FISHING IN CLOSE PROXIMITY
</p>

<p class="text-weight-l text-size-m">1. General</p>
<p>
	The lights mentioned herein shall, if exhibited in pursuance of Rule 26(d), be
	placed where they can best be seen. They shall be at least 0.9 metre apart but
	at a lower level than lights prescribed in Rule 26(b)(i) and (c)(i). The
	lights shall be visible all round the horizon at a distance of at least 1 mile
	but at a lesser distance than the lights prescribed by these Rules for fishing
	vessels.
</p>
<p class="text-weight-l text-size-m">2. Signals for trawlers</p>
<p>
	(a) Vessels of 20 m of more in length when engaged in trawling, whether using
	demersal or pelagic gear shall exhibit:
</p>
<ul>
	<li>(i) when shooting their nets : two white lights in a vertical line;</li>
	<li>
		(ii) when hauling their nets : one white light over one red light in a
		vertical line;
	</li>
	<li>
		(iii) when the net has come fast upon an obstruction : two red lights in a
		vertical line.
	</li>
</ul>
<p>
	(b) Each vessel of 20 m or more in length engaged in pair trawling shall
	exhibit :
</p>
<ul>
	<li>
		(i) by night, a searchlight directed forward and in the direction of the
		other vessel of the pair;
	</li>
	<li>
		(ii) when shooting or hauling their nets or when their nets have come fast
		upon an obstruction, the lights prescribed in 2(a) above.
	</li>
</ul>
<p>
	(c) A vessel of less than 20 m in length engaged in trawling, whether using
	demersal or pelagic gear or engaged in pair trawling, may exhibit the lights
	prescribed in paragraphs (a) or (b) of this section, as appropriate.
</p>
<p class="text-weight-l text-size-m">3. Signals for purse seiners</p>
<p>
	Vessels engaged in fishing with purse seine gear may exhibit two yellow lights
	in a vertical line. These lights shall flash alternately every second and with
	equal light and occultation duration. These lights may be exhibited only when
	the vessel is hampered by its fishing gear.
</p>
